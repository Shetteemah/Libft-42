#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*result;
	unsigned int	i;

	if (s == NULL)
		return (NULL);
	result = (char *) malloc(ft_strlen(s) +1);
	if (!(result))
		return (NULL);
	i = 0;
	while (s[i])
	{
		result[i] = (*f)(i, s[i]);
		i++;
	}
	result[ft_strlen(s)] = '\0';
	return (result);
}
